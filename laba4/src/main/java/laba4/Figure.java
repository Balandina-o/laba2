package laba4;

public abstract class Figure {
    private String name; // имя фигуры
    private int Storon ;
    public abstract int Perimeter(); // определение площади
    public abstract double Area();
    public String GetName() {
        return name;
    }
    public void setName(String name){
        this.name=name;
    }

    public int getStoron() {
        return Storon;
    }

    public void setStoron(int storon) {
        Storon = storon;
    }
}